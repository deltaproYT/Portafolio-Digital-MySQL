CREATE TABLE subjects (
idsubjects int AUTO_INCREMENT NOT NULL PRIMARY KEY,
subject VARCHAR(100) NOT NULL,
difficulty VARCHAR(50),
finished boolean NOT NULL
);

--------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------

CREATE TABLE intermediate_usr_sbj (
idusr_sbj int AUTO_INCREMENT NOT NULL PRIMARY KEY,
idsubjects int,
idusers int,
FOREIGN KEY (idsubjects) REFERENCES subjects(idsubjects),
FOREIGN KEY (idusers) REFERENCES users(idusers),
UNIQUE (idusers, idsubjects)
);

